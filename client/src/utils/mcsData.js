// Helper function to extract postcode prefix
export function getPostcodePrefix(postcode) {
  if (!postcode) return null;
  // Remove spaces and convert to uppercase
  const clean = postcode.replace(/\s/g, '').toUpperCase();
  // Extract letters only from the beginning
  const match = clean.match(/^[A-Z]+/);
  return match ? match[0] : null;
}

// Helper function to get data from postcode
export function getMCSDataFromPostcode(postcode) {
  const prefix = getPostcodePrefix(postcode);
  if (!prefix || !MCS_POSTCODE_DATA[prefix]) {
    return null;
  }
  return MCS_POSTCODE_DATA[prefix];
}